from exceptions import EmptyQueueException

class Queue:
    def __init__(self):
        self.items = []

    def is_empty(self):
        return self.items == []

    def enqueue(self, item):
        self.items.append(item)

    def dequeue(self):
        if not self.is_empty():
            return self.items.pop(0)
        else:
            raise EmptyQueueException("Queue is empty")

    def size(self):
        return len(self.items)
